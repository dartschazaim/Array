#ifndef ARRAY_H
#define ARRAY_H

#include <locale.h>
#include <time.h>
#include <math.h>

/**
 * Задача 1: Отношение четных к положительным элементам массива
 * Генерирует массив случайных чисел, подсчитывает количество положительных и четных элементов,
 * вычисляет и выводит отношение четных к положительным элементам
 * @param Нет параметров
 * @return void
 */
void task1();

/**
 * Заполнение массива целыми числами в заданном диапазоне
 * @param arr указатель на массив для заполнения
 * @param size размер массива
 * @param minVal минимальное значение для генерации
 * @param maxVal максимальное значение для генерации
 * @return void
 */
void fillArray(int arr[], int size, int minVal, int maxVal);

/**
 * Вывод массива целых чисел с индексами
 * @param arr указатель на массив для вывода
 * @param size размер массива
 * @return void
 */
void printArray(int arr[], int size);

/**
 * Подсчет элементов массива, значения которых не входят в заданный диапазон
 * @param arr указатель на массив
 * @param size размер массива
 * @param K начальный индекс диапазона
 * @param L конечный индекс диапазона
 * @param A нижняя граница значений
 * @param B верхняя граница значений
 * @return количество элементов вне диапазона значений
 */
int countInRange(int arr[], int size, int K, int L, int A, int B);

/**
 * Задача 2: Элементы вне заданных диапазонов
 * Создает массив случайных чисел, запрашивает у пользователя диапазоны индексов и значений,
 * подсчитывает и выводит элементы, не входящие в заданный диапазон значений
 * @param Нет параметров
 * @return void
 */
void task2();

/**
 * Заполнение массива вещественными числами в заданном диапазоне
 * @param arr указатель на массив для заполнения
 * @param size размер массива
 * @param minVal минимальное значение для генерации
 * @param maxVal максимальное значение для генерации
 * @return void
 */
void fillArrayDouble(double arr[], int size, double minVal, double maxVal);

/**
 * Вывод массива вещественных чисел с индексами
 * @param arr указатель на массив для вывода
 * @param size размер массива
 * @param name имя массива для вывода в заголовке
 * @return void
 */
void printArrayDouble(double arr[], int size, const char* name);

/**
 * Обработка трех массивов вещественных чисел:
 * - Для массива A: замена значений на их дробные части
 * - Для массива B: замена значений на их целые части
 * - Для массива C: замена отрицательных значений на их модули
 * Также вычисляет статистику по массивам
 * @param a указатель на первый массив
 * @param b указатель на второй массив
 * @param c указатель на третий массив
 * @param n размер первого массива
 * @param m размер второго массива
 * @param k размер третьего массива
 * @return void
 */
void processArrays(double a[], double b[], double c[], int n, int m, int k);

/**
 * Задача 3: Обработка вещественных массивов
 * Создает три массива случайных вещественных чисел и применяет к ним различные преобразования:
 * дробные части для первого, целые части для второго, модули для третьего
 * @param Нет параметров
 * @return void
 */
void task3();

/**
 * Сортировка массива методом выбора
 * @param arr указатель на массив для сортировки
 * @param n размер массива
 * @return void
 */
void selectionSort(int arr[], int n);

/**
 * Сортировка массива пузырьковым методом
 * @param arr указатель на массив для сортировки
 * @param n размер массива
 * @return void
 */
void bubbleSort(int arr[], int n);

/**
 * Сортировка массива шейкерным (коктейльным) методом
 * @param arr указатель на массив для сортировки
 * @param n размер массива
 * @return void
 */
void cocktailSort(int arr[], int n);

/**
 * Сортировка массива методом вставок
 * @param arr указатель на массив для сортировки
 * @param n размер массива
 * @return void
 */
void insertionSort(int arr[], int n);

/**
 * Генерация массива случайных целых чисел
 * @param arr указатель на массив для заполнения
 * @param n размер массива
 * @return void
 */
void generateRandomArray(int arr[], int n);

/**
 * Копирование содержимого одного массива в другой
 * @param source указатель на исходный массив
 * @param dest указатель на целевой массив
 * @param n размер массивов
 * @return void
 */
void copyArray(int source[], int dest[], int n);

/**
 * Тестирование алгоритма сортировки с измерением времени выполнения
 * @param sortFunc указатель на функцию сортировки
 * @param arr указатель на массив для сортировки
 * @param n размер массива
 * @param name название алгоритма сортировки для вывода
 * @return void
 */
void testSort(void (*sortFunc)(int[], int), int arr[], int n, const char* name);

/**
 * Задача 4: Сравнение алгоритмов сортировки
 * Сравнивает время выполнения четырех алгоритмов сортировки (выбором, пузырьком, шейкерной, вставками)
 * на массивах различных размеров и выводит результаты сравнения
 * @param Нет параметров
 * @return void
 */
void task4();

#endif // ARRAY_H